        'proof_spec_path': None,
        'public_proof_url': None,
        'distribution_status': 'frozen',
        'last_stage_advance_at': now,
        'last_artifact_change_at': None,
        'kill_criteria_hit': False,
        'switch_review_required': True,
        'status': 'frozen'
    }
}
for op in queue['opportunities']:
    pid = op['id']
    if pid in status_map:
        op['portfolio_status'] = status_map[pid]
    if pid in proof_map:
        op.update(proof_map[pid])
queue['updated_at'] = now
queue['selected_opportunity'] = 'agent_security_scanner'
queue['portfolio_strategy'] = 'one_primary_one_reserve'
queue['active_wedge'] = 'agent_security_scanner'
queue['reserve_wedge'] = 'base_account_miniapp_probe'
queue['maintenance_wedges'] = ['base_gas_tracker_v2', 'contextkeeper_mvp']
queue['frozen_reference_projects'] = [
    'x402_paid_api_demo', 'erc8004_registry_utility', 'bankr_operator_console',
    'base-beginner-guide', 'base-portfolio', 'base-send', 'base-receive',
    'base_gas_tracker_static_duplicate', 'base_gas_tracker_builder_duplicate'
]
queue_path.write_text(json.dumps(queue, indent=2) + '\n')
PY

{
  echo "# Portfolio Tribunal Refresh"
  echo
  echo "- timestamp: $(date -u +%Y-%m-%dT%H:%M:%SZ)"
  echo "- ledger: state/project-ledger.json"
  echo "- queue: state/base-opportunity-queue.json"
  echo
  echo "## Result"
  echo "- primary: agent_security_scanner"
  echo "- reserve: base_account_miniapp_probe"
  echo "- maintain: base_gas_tracker_v2, contextkeeper_mvp"
  echo "- frozen: x402_paid_api_demo, erc8004_registry_utility, bankr_operator_console, generic/reference projects"
} > "$REPORT"

echo "PORTFOLIO_TRIBUNAL_REFRESH_OK $REPORT"
