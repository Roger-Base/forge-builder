#!/usr/bin/env bash
# Refresh deterministic 30-day portfolio state.

set -euo pipefail

WORKSPACE="${OPENCLAW_WORKSPACE:-$HOME/.openclaw/workspace}"
LEDGER="$WORKSPACE/state/project-ledger.json"
QUEUE="$WORKSPACE/state/base-opportunity-queue.json"
REPORT="$WORKSPACE/state/runtime/portfolio-tribunal-$(date -u +%Y%m%d-%H%M%S).md"
MODE="--refresh"

usage() {
  cat <<USAGE
Usage:
  $0 --validate
  $0
  $0 --agent-help
USAGE
}

agent_help() {
  cat <<HELP
portfolio-tribunal refreshes or validates Roger's fixed 30-day portfolio.

Validation checks:
- exactly one ACTIVE_PRIMARY
- exactly one ACTIVE_RESERVE
- exactly two MAINTAIN_PROOF wedges

Refresh mode rewrites base-opportunity-queue.json to match the tribunal decision.
HELP
}

case "${1:-}" in
  --validate)
    MODE="--validate"
    ;;
  -h|--help)
    usage
    exit 0
    ;;
  --agent-help)
    agent_help
    exit 0
    ;;
esac

if [[ "$MODE" == "--validate" ]]; then
  jq -e '
    .primary_id == "agent_security_scanner"
    and .reserve_id == "base_account_miniapp_probe"
    and ([.projects[] | select(.status == "ACTIVE_PRIMARY")] | length) == 1
    and ([.projects[] | select(.status == "ACTIVE_RESERVE")] | length) == 1
    and ([.projects[] | select(.status == "MAINTAIN_PROOF")] | length) == 2
  ' "$LEDGER" >/dev/null
  echo "PORTFOLIO_TRIBUNAL_VALIDATE_OK $LEDGER"
  exit 0
fi

WORKSPACE_ENV="$WORKSPACE" python3 - <<'PY'
