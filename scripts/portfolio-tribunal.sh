from datetime import datetime, timezone

ws = pathlib.Path(os.environ["WORKSPACE_ENV"])
now = datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")
ledger = json.loads((ws/'state/project-ledger.json').read_text())
queue_path = ws/'state/base-opportunity-queue.json'
queue = json.loads(queue_path.read_text())
status_map = {p['id']: p['status'] for p in ledger['projects']}
proof_map = {
    'agent_security_scanner': {
        'stage': 'PROOF_SPEC',
        'research_packet_path': 'docs/wedges/agent_security_scanner/research-packet.md',
        'proof_spec_path': 'docs/wedges/agent_security_scanner/proof-spec.md',
        'public_proof_url': None,
        'distribution_status': 'not_started',
        'last_stage_advance_at': now,
        'last_artifact_change_at': now,
        'kill_criteria_hit': False,
        'switch_review_required': False,
        'next_action': {
            'type': 'build_primary_wedge',
            'command': 'cd ~/.openclaw/workspace && bash scripts/agent-security-scanner.sh --sample --output docs/wedges/agent_security_scanner/sample-audit.md',
            'target': 'docs/wedges/agent_security_scanner/sample-audit.md',
            'proof_expected': 'sample security audit generated by the canonical scanner script'
        },
        'status': 'active'
    },
    'base_account_miniapp_probe': {
        'stage': 'RESEARCH_PACKET',
        'research_packet_path': 'docs/wedges/base_account_miniapp_probe/research-packet.md',
        'proof_spec_path': 'docs/wedges/base_account_miniapp_probe/proof-spec.md',
        'public_proof_url': None,
        'distribution_status': 'not_started',
        'last_stage_advance_at': now,
        'last_artifact_change_at': None,
        'kill_criteria_hit': False,
        'switch_review_required': True,
        'status': 'queued'
    },
    'base_gas_tracker_v2': {
        'stage': 'MAINTAIN',
        'research_packet_path': None,
        'proof_spec_path': None,
        'public_proof_url': 'https://roger-base.github.io/base-gas-tracker/',
        'distribution_status': 'proof_anchor_live',
        'last_stage_advance_at': now,
        'last_artifact_change_at': '2026-03-07T23:34:16Z',
        'kill_criteria_hit': False,
        'switch_review_required': False,
        'status': 'maintain'
    },
    'contextkeeper_mvp': {
        'stage': 'MAINTAIN',
        'research_packet_path': None,
        'proof_spec_path': None,
        'public_proof_url': None,
        'distribution_status': 'internal_first',
        'last_stage_advance_at': now,
        'last_artifact_change_at': '2026-03-07T19:35:00Z',
        'kill_criteria_hit': False,
        'switch_review_required': False,
        'status': 'maintain'
    },
    'x402_paid_api_demo': {
        'stage': 'FROZEN',
        'research_packet_path': None,
        'proof_spec_path': None,
        'public_proof_url': None,
        'distribution_status': 'frozen',
        'last_stage_advance_at': now,
        'last_artifact_change_at': None,
        'kill_criteria_hit': False,
        'switch_review_required': True,
        'status': 'frozen'
    },
    'erc8004_registry_utility': {
        'stage': 'FROZEN',
        'research_packet_path': None,
        'proof_spec_path': None,
        'public_proof_url': None,
        'distribution_status': 'frozen',
        'last_stage_advance_at': now,
