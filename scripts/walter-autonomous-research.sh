    local task_id="$1"
    local description="$2"
    local target="$3"
    local output_file="$OUTPUT_DIR/research/protocol-analysis-$(get_date_stamp)-$(echo $task_id | cut -d- -f2).md"

    log "Starting protocol analysis: $description"
    
