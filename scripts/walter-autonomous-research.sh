# Main execution flow
run_autonomous_research() {
    init

    if ! check_prerequisites; then
        exit 0
    fi

    # Get next task
    local task_json=$($MONITOR next-task 2>/dev/null | jq -r . 2>/dev/null || echo "")
    if [[ -z "$task_json" || "$task_json" == "null" || "$task_json" == "No tasks queued" ]]; then
        log "${YELLOW}No valid tasks found${NC}"
        exit 0
    fi

    local task_id=$(echo "$task_json" | jq -r '.id')
    local task_type=$(echo "$task_json" | jq -r '.type')
    local task_desc=$(echo "$task_json" | jq -r '.description')
    local task_target=$(echo "$task_json" | jq -r '.target // ""')

    log "${BLUE}Starting task: $task_id${NC}"
    log "${BLUE}Type: $task_type${NC}"
    log "${BLUE}Description: $task_desc${NC}"

    # Mark task as started
    $MONITOR start-task "$task_id" 2>/dev/null

    # Execute based on type
    local output_file=""
    case "$task_type" in
        protocol_analysis)
            output_file=$(execute_protocol_analysis "$task_id" "$task_desc" "$task_target")
            ;;
        ecosystem_mapping)
            output_file=$(execute_ecosystem_mapping "$task_id" "$task_desc" "$task_target")
            ;;
        gap_analysis)
            output_file=$(execute_gap_analysis "$task_id" "$task_desc" "$task_target")
            ;;
        *)
            log "${YELLOW}Unknown task type: $task_type${NC}"
            # Generic research output
            output_file="$OUTPUT_DIR/research/generic-$(get_date_stamp)-$(echo $task_id | cut -d- -f2).md"
            cat > "$output_file" << EOF
# Research: $task_desc

**Task ID:** $task_id  
**Type:** $task_type  
**Executed:** $(get_timestamp)  
**Autonomous:** Yes  

## Framework Applied: R-A-D-S Cycle

## Research Output

\[Research to be completed following framework at /Users/roger/.openclaw/workspace/walter/research-framework.md\]

---

*Generated by Walter Autonomous Research System*
EOF
            ;;
    esac

    # Mark task as completed
    $MONITOR complete-task "$task_id" "$output_file" 2>/dev/null

    log "${GREEN}✓ Autonomous research completed${NC}"
    log "${GREEN}  Output: $output_file${NC}"

    # Create handoff summary
    local handoff_file="$STATE_DIR/walter-handoff-research-$(get_date_stamp).json"
    cat > "$handoff_file" << EOF
{
  "timestamp": "$(get_timestamp)",
  "type": "autonomous_research",
  "taskId": "$task_id",
  "taskType": "$task_type",
  "description": "$task_desc",
  "outputFile": "$output_file",
  "executedWhileRogerIdle": true,
  "requiresResponse": false,
  "summary": "Completed $task_type research: $task_desc",
  "forRoger": "Review output file for findings and recommendations"
}
EOF

    log "${CYAN}📋 Created handoff: $handoff_file${NC}"
    log "${GREEN}✓ Ready for Roger review when he returns${NC}"
}

# Show status
show_status() {
    log "Walter Autonomous Research System Status"
    log "========================================"
    
    # Check Roger status
    local idle_status=$($MONITOR status 2>/dev/null)
    log "Roger status: $idle_status"
    
    # Check queue
    local next_task=$($MONITOR next-task 2>/dev/null)
    log "Next task: $next_task"
    
    # List recent outputs
    log ""
    log "Recent research outputs:"
    ls -lt "$OUTPUT_DIR/research/" 2>/dev/null | head -5 || log "No outputs yet"
}

# Command handler
case "${1:-}" in
    run)
        run_autonomous_research
        ;;
    status)
        show_status
        ;;
    help|--help|-h)
        echo "Walter Autonomous Research Trigger"
        echo ""
        echo "Commands:"
        echo "  run      - Execute research if Roger idle and tasks queued"
        echo "  status   - Show system status"
        echo ""
        echo "This script integrates with:"
        echo "  - walter-coordination-monitor.sh (track Roger availability)"
        echo "  - walter/research-framework.md (research methodology)"
        echo ""
        echo "Usage:"
        echo "  Add tasks: ./walter-coordination-monitor.sh add-task \<type\> \<desc\>"
        echo "  Run research: ./walter-autonomous-research.sh run"
        ;;
    *)
        show_status
        ;;
esac
